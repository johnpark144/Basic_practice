// spread : 양수면 그림자를 확장하고, 음수면 축소한다.
// color : 그림자 색을 정한다.
// inset : 그림자를 요소의 안쪽에 만든다.
// initial : 기본값으로 설정한다.
// inherit : 부모 요소의 값을 상속받는다.

